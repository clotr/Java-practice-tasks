public class TwoNumbers {
    public static void main(String[] args) {
        int x = 13;
        int y = 22;
        System.out.println("13 + 22 = " + (x + y));
        System.out.println("13 - 22 = " + (x - y));
        System.out.println("13 * 22 = " + (x * y));
        System.out.println("13 / 22 = " + (float) x / (float) y);
        System.out.println("22 / 13 = " + (y / x));
        System.out.println("22 % 13 = " + (y % x));
    }
}
